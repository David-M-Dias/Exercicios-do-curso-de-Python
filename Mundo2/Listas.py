Lista = [1,2,3] # Criando lista e adicionando valores a mesma.

print(len(Lista)) # Contando quantidade de itens na lista.

print(Lista) # imprimindo a lista.

Lista = Lista + [4,5,6] # Adicionando novos valores a uma lista já existente.

print(Lista) # imprimindo lista atualizada.

print(Lista[:3]) # imprimindo os valores desde a posição 0 até a posição 3(obs: a que a terceira posição não será impressa.)

print(Lista[:4]) # imprimindo os valores desde a posição inicial (0) até a posição 4 (obs: Para aqui sim imprimir a posição 3.)

